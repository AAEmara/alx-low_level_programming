/**
* add - Adds two integers and return the result.
*
* @a: Integer type parameter, which is the first number to be added.
* @b: Integer type parameter, which is the second number to be added.
*
* Return: Integer Type Value.
*/
int add(int a, int b)
{
	int sum;

	sum = a + b;
	return (sum);
}
